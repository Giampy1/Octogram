package it.octogram.android;

public class ConfigProperty<T> {

    private final String key;
    private T value;

    public ConfigProperty(String key, T value) {
        this.key = key;
        this.value = value;
    }

    public String getKey() {
        return key;
    }

    public T getValue() {
        return value;
    }

    protected void setValue(T newValue) {
        value = newValue;
    }
}
